import { useState, useEffect } from "react";
import { Button } from "@/components/ui/button";
import { X, User, ChevronRight } from "lucide-react";
import { cn } from "@/lib/utils";
import GoogleSignIn from "./GoogleSignIn";
import { useAuth } from "@/context/AuthContext";

interface GoogleAccount {
    name: string;
    email: string;
    image?: string;
}

interface GoogleAuthSimulationProps {
    isOpen: boolean;
    onClose: () => void;
    onSelectAccount: (account: GoogleAccount) => void;
    isSignup?: boolean;
}

const mockAccounts: GoogleAccount[] = [

    { name: "Demo User", email: "demo@gmail.com" }
];

export default function GoogleAuthSimulation({ isOpen, onClose, onSelectAccount, isSignup }: GoogleAuthSimulationProps) {
    const [isLoading, setIsLoading] = useState(false);
    const { handleGoogleCredential } = useAuth();

    if (!isOpen) return null;

    const handleSelect = (account: GoogleAccount) => {
        setIsLoading(true);
        // Simulate "Authorizing..." delay
        setTimeout(() => {
            onSelectAccount(account);
            setIsLoading(false);
        }, 1500);
    };

    return (
        <div className="fixed inset-0 z-[100] flex items-center justify-center p-4 bg-black/60 backdrop-blur-sm animate-fade-in">
            <div className="bg-white text-[#3c4043] w-full max-w-[400px] rounded-lg shadow-2xl overflow-hidden animate-scale-in font-sans">
                {/* Google Logo & Header */}
                <div className="p-8 text-center border-b border-gray-100">
                    <div className="flex justify-center mb-4">
                        <svg className="w-12 h-12" viewBox="0 0 24 24">
                            <path
                                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                fill="#4285F4"
                            />
                            <path
                                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                fill="#34A853"
                            />
                            <path
                                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l3.66-2.84z"
                                fill="#FBBC05"
                            />
                            <path
                                d="M12 5.38c1.62 0 3.06.56 4.21 1.66l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                fill="#EA4335"
                            />
                        </svg>
                    </div>
                    <h1 className="text-2xl font-medium mb-2">Choose an account</h1>
                    <p className="text-sm text-gray-600">to continue to <span className="font-bold text-gray-800">Arrows.in</span></p>
                </div>

                <div className="p-2 max-h-[400px] overflow-y-auto bg-gray-50/50">
                    {isLoading ? (
                        <div className="py-20 flex flex-col items-center gap-4 animate-pulse">
                            <div className="w-12 h-12 border-4 border-blue-500 border-t-transparent rounded-full animate-spin"></div>
                            <p className="text-sm font-medium text-blue-600">Authorizing {isSignup ? "Sign Up" : "Login"}...</p>
                        </div>
                    ) : (
                        <div className="space-y-1">
                            {mockAccounts.map((account) => (
                                <button
                                    key={account.email}
                                    onClick={() => handleSelect(account)}
                                    className="w-full flex items-center gap-4 p-3 hover:bg-gray-100 transition-colors rounded-md text-left group"
                                >
                                    <div className="w-8 h-8 rounded-full bg-blue-100 flex items-center justify-center text-blue-600">
                                        <User className="w-5 h-5" />
                                    </div>
                                    <div className="flex-grow">
                                        <p className="text-sm font-medium leading-none">{account.name}</p>
                                        <p className="text-xs text-gray-500 mt-1">{account.email}</p>
                                    </div>
                                    <ChevronRight className="w-4 h-4 text-gray-300 group-hover:text-gray-500" />
                                </button>
                            ))}
                            <div className="mt-2 border-t border-gray-100 pt-4 px-1">
                                <GoogleSignIn
                                    onSuccess={(credential) => {
                                        setIsLoading(true);
                                        setTimeout(() => {
                                            handleGoogleCredential(credential, isSignup);
                                            setIsLoading(false);
                                        }, 1000);
                                    }}
                                />
                            </div>
                        </div>
                    )}
                </div>

                <div className="p-4 bg-white text-[11px] text-gray-500 border-t border-gray-100">
                    To continue, Google will share your name, email address, language preference, and profile picture with Arrows.in
                    <div className="mt-4 flex gap-4">
                        <a href="/legal#privacy" target="_blank" rel="noopener noreferrer" className="hover:underline text-blue-600">Privacy Policy</a>
                        <a href="/legal#terms" target="_blank" rel="noopener noreferrer" className="hover:underline text-blue-600">Terms of Service</a>
                    </div>
                </div>

                <button
                    onClick={onClose}
                    className="absolute top-4 right-4 text-gray-400 hover:text-gray-600 transition-colors"
                >
                    <X className="w-5 h-5" />
                </button>
            </div>
        </div>
    );
}
